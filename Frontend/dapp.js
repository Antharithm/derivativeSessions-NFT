const contract_address = "0xEABE657785f4366f2e517C6e0e7E46C13E638B91";

const dApp = {
  ethEnabled: function() {
    // If the browser has MetaMask installed
    if (window.ethereum) {
      window.web3 = new Web3(window.ethereum);
      window.ethereum.enable();
      return true;
    }
    return false;
  },
 
  registerArtwork: async function() {
   

    try {

     
        this.contract.methods.registerArtwork("0x7EC309FB3ca2e6D0F4Bf6e161b66195bfF163fA7").send({from: this.accounts[0]})
        .on("receipt", (receipt) => {
          M.toast({ html: "Transaction Mined! Refreshing UI..." });
          location.reload();
        });
      

    } catch (e) {
      console.log(e)
      alert("ERROR:", JSON.stringify(e));
    }
  },
  main: async function() {
    // Initialize web3
    if (!this.ethEnabled()) {
      alert("Please install MetaMask to use this dApp!");
    }

    this.accounts = await window.web3.eth.getAccounts();

    this.derivativeSessionsABI = derivative_sessions_abi;

    this.contract = new window.web3.eth.Contract(
      this.derivativeSessionsABI,
      contract_address,
      { defaultAccount: this.accounts[0] }
    );
    console.log("Contract object", this.contract);

    this.updateUI();
  }
};

dApp.main();
